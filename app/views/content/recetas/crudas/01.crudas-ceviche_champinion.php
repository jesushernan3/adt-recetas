    <h3>
      Preparación
    </h3>
    <ul>
      <li>Para esta receta de comida vegana los pasos son muy sencillos. Primero vamos a tomar todos los vegetales y los
        vamos a lavar muy bien.</li>
      <li>Ahora vamos a picar los ingredientes uno a uno e iremos colocando en un recipiente, ojalá de vidrio. El vidrio
        almacena mejor la temperatura, el sabor y la absorción de los distintos gustos de los ingredientes.</li>
      <li>Tomamos los champiñones o setas y los cortamos en láminas. Si son champiñones pequeños sólo córtalos a la
        mitad.</li>
      <li>Pelamos los tomates y picamos en cuadritos o cubitos.</li>
      <li>La cebolla la cortaremos en tiritas largas finas estilo juliana.</li>
      <li>El chile (ají, pimiento) es opcional si es que quieres que tu ceviche quede más picante. Si decides usarlo
        picalo bien fino en tiras o cuadritos.</li>
      <li>Ahora vamos a mezclar. Primero revuelve muy bien todos los ingredientes, luego, agrega el jugo de los tres
        limones, además de un chorro de vinagre de vino tinto, aceite de oliva y sal a gusto, vuelve a revolver muy
        bien.</li>
      <li>Finalmente, pica el cilantro y agrega al ceviche, revuelve muy bien por última vez y deja tu preparación ya
        terminada en refrigeración.</li>
      <li>Refrigerar por lo menos unas dos horas para que los ingredientes se pasen bien con le mezcla líquida, además
        de combinar los sabores de todos los vegetales.</li>
    </ul>
  </section>

  <section class="section content">
