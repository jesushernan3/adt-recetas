 <section class="section">
  <h3>
    Preparación
  </h3>

  <ul>
    <li>Antes de cualquier paso de esta rica preparación, debemos tomar los garbanzos, lavarlos muy bien y luego dejarlos remojar en un recipiente con agua fría por lo menos unas 12 horas. Mi recomendación es que la noche anterior las dejas remojando tranquilamente.</li>
    <li>Al día siguientes puedes cocerlos en agua con sal o bien usarlos crudos. Al hacerlo con garbanzos crudos se asimila mejor la absorción de los nutrientes de esta legumbre.</li>
    <li>Ahora, comenzaremos de lleno la preparación de nuestro rico hummus. Usando una licuadora (batidora o juguera), vamos a moler los garbanzos con el diente de ajo, agregando muy de a poco agua y el tahini.</li>
    <li>Una vez que ya pusimos todos los garbanzos y toda el agua, agregaremos un chorro de aceite, pimienta, comino, una pizca de sal y al final de nuestra mezcla, el jugo de limón.</li>
    <li>La idea es que este puré de crema de garbanzos quede en un punto medio de espesor.</li>
    <li>Ahora servimos el hummus en un plato y encima ponemos un chorrito de aceite de oliva, y especias a gusto.</li>
    <li>De por si el hummus encima lleva perejil y aliño, otras veces se pueden variar con aceitunas, guisantes, etcétera. Teniendo la receta tradicional ya puedes jugar a combinar esta rica preparación como desees.</li>
